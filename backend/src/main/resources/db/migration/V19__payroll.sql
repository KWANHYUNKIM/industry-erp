CREATE TABLE public.payslips (
    id               bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at       timestamp(6) without time zone,
    updated_at       timestamp(6) without time zone,
    employee_id      bigint NOT NULL,
    -- 귀속월 'YYYY-MM'
    pay_month        character varying(7) NOT NULL,
    base_salary      numeric(18,2) NOT NULL DEFAULT 0,
    allowance_total  numeric(18,2) NOT NULL DEFAULT 0,
    deduction_total  numeric(18,2) NOT NULL DEFAULT 0,
    net_pay          numeric(18,2) NOT NULL DEFAULT 0,
    -- 작성(DRAFT) → 확정(CONFIRMED)
    status           character varying(20) NOT NULL DEFAULT 'DRAFT',
    remark           character varying(300),
    created_by       character varying(50),
    CONSTRAINT fk_payslips_employee_id FOREIGN KEY (employee_id) REFERENCES public.employees(id),
    CONSTRAINT ck_payslips_status CHECK (status IN ('DRAFT', 'CONFIRMED')),
    -- 사원당 같은 귀속월 급여명세는 1건만.
    CONSTRAINT uk_payslips_employee_month UNIQUE (employee_id, pay_month)
);
CREATE INDEX idx_payslips_employee_id ON public.payslips (employee_id);
CREATE INDEX idx_payslips_pay_month   ON public.payslips (pay_month);

CREATE TABLE public.payslip_lines (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    payslip_id  bigint NOT NULL,
    -- ALLOWANCE(수당) / DEDUCTION(공제)
    kind        character varying(20) NOT NULL,
    name        character varying(100) NOT NULL,
    amount      numeric(18,2) NOT NULL DEFAULT 0,
    -- 4대보험 등 자동계산 항목이면 true (사용자가 지우지 못하게 화면에서 구분)
    auto        boolean NOT NULL DEFAULT false,
    line_no     integer NOT NULL DEFAULT 1,
    CONSTRAINT fk_payslip_lines_payslip_id FOREIGN KEY (payslip_id)
        REFERENCES public.payslips(id) ON DELETE CASCADE,
    CONSTRAINT ck_payslip_lines_kind CHECK (kind IN ('ALLOWANCE', 'DEDUCTION')),
    CONSTRAINT ck_payslip_lines_amount CHECK (amount >= 0)
);
CREATE INDEX idx_payslip_lines_payslip_id ON public.payslip_lines (payslip_id);
