-- 사원(employees)에 기본급을 두고, 사원+귀속월 단위로 급여명세를 만든다.
-- 실지급액 = 기본급 + 수당합 − 공제합. 공제는 4대보험을 자동 계산하고 수동 항목을 더한다.

ALTER TABLE public.employees ADD COLUMN base_salary numeric(18,2) NOT NULL DEFAULT 0;

