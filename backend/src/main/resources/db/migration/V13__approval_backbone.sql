-- ── 2. 기안서 확장 ────────────────────────────────────────────────────
ALTER TABLE public.approval_documents ADD COLUMN form_template_id bigint;
ALTER TABLE public.approval_documents ADD COLUMN draft_no   character varying(30);
ALTER TABLE public.approval_documents ADD COLUMN form_data  jsonb NOT NULL DEFAULT '{}'::jsonb;
ALTER TABLE public.approval_documents ADD COLUMN department character varying(100);
ALTER TABLE public.approval_documents ADD COLUMN project_id bigint;
ALTER TABLE public.approval_documents ADD COLUMN deleted    boolean NOT NULL DEFAULT false;
ALTER TABLE public.approval_documents ADD COLUMN deleted_at timestamp(6) without time zone;

-- form_type(enum 문자열) -> 양식 마스터 FK 백필. 코드가 그대로 일치한다.
UPDATE public.approval_documents d
   SET form_template_id = t.id
  FROM public.approval_form_templates t
 WHERE t.code = d.form_type
   AND d.form_template_id IS NULL;

-- 기안No. 백필: 기안일자별 일련번호 (이카운트 표기 '2026/07/10-2')
WITH numbered AS (
    SELECT id,
           to_char(draft_date, 'YYYY/MM/DD') || '-' ||
           row_number() OVER (PARTITION BY draft_date ORDER BY id) AS dno
      FROM public.approval_documents
     WHERE draft_no IS NULL
)
UPDATE public.approval_documents d
   SET draft_no = n.dno
  FROM numbered n
 WHERE d.id = n.id;

-- 백필이 끝났으므로 이제 필수로 굳힌다.
ALTER TABLE public.approval_documents ALTER COLUMN form_template_id SET NOT NULL;
ALTER TABLE public.approval_documents ALTER COLUMN draft_no SET NOT NULL;

ALTER TABLE public.approval_documents
    ADD CONSTRAINT uk_approval_documents_draft_no UNIQUE (draft_no);
ALTER TABLE public.approval_documents
    ADD CONSTRAINT fk_approval_documents_form_template_id
    FOREIGN KEY (form_template_id) REFERENCES public.approval_form_templates(id);
ALTER TABLE public.approval_documents
    ADD CONSTRAINT fk_approval_documents_project_id
    FOREIGN KEY (project_id) REFERENCES public.projects(id);

CREATE INDEX idx_approval_documents_form_template_id ON public.approval_documents (form_template_id);
CREATE INDEX idx_approval_documents_project_id       ON public.approval_documents (project_id);

-- ── 3. 수신참조 / 공유자 ──────────────────────────────────────────────
-- 결재자는 approval_lines(순차 결재)에 있다. 이 테이블은 결재권이 없는 참여자만 담는다.
CREATE TABLE public.approval_participants (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    document_id bigint NOT NULL,
    user_id     bigint NOT NULL,
    role        character varying(20) NOT NULL,
    CONSTRAINT fk_approval_participants_document_id FOREIGN KEY (document_id)
        REFERENCES public.approval_documents(id) ON DELETE CASCADE,
    CONSTRAINT fk_approval_participants_user_id FOREIGN KEY (user_id)
        REFERENCES public.users(id),
    CONSTRAINT ck_approval_participants_role CHECK (role IN ('REFERENCE', 'SHARE')),
    CONSTRAINT uk_approval_participants UNIQUE (document_id, user_id, role)
);
CREATE INDEX idx_approval_participants_document_id ON public.approval_participants (document_id);
CREATE INDEX idx_approval_participants_user_id     ON public.approval_participants (user_id);

-- ── 4. 기안서 ↔ ERP 전표 연결 ─────────────────────────────────────────
-- 이카운트 '검수확인서' 하단에 구매전표가 통째로 붙고, 목록엔 'ERP전표(건)'가 뜬다.
--
-- 전표 종류별로 컬럼을 따로 두고 CHECK 로 '정확히 하나'를 강제한다.
-- voucher_type + voucher_id 같은 다형 참조는 FK 를 걸 수 없어 고아 행을 막지 못한다.
CREATE TABLE public.approval_document_vouchers (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at  timestamp(6) without time zone,
    document_id bigint NOT NULL,
    sales_id    bigint,
    purchase_id bigint,
    expense_id  bigint,
    CONSTRAINT fk_adv_document_id FOREIGN KEY (document_id)
        REFERENCES public.approval_documents(id) ON DELETE CASCADE,
    CONSTRAINT fk_adv_sales_id    FOREIGN KEY (sales_id)    REFERENCES public.sales(id),
    CONSTRAINT fk_adv_purchase_id FOREIGN KEY (purchase_id) REFERENCES public.purchases(id),
    CONSTRAINT fk_adv_expense_id  FOREIGN KEY (expense_id)  REFERENCES public.expenses(id),
    CONSTRAINT ck_adv_exactly_one_voucher
        CHECK (num_nonnulls(sales_id, purchase_id, expense_id) = 1)
);
CREATE INDEX idx_adv_document_id ON public.approval_document_vouchers (document_id);
CREATE INDEX idx_adv_sales_id    ON public.approval_document_vouchers (sales_id);
CREATE INDEX idx_adv_purchase_id ON public.approval_document_vouchers (purchase_id);
CREATE INDEX idx_adv_expense_id  ON public.approval_document_vouchers (expense_id);

-- 같은 전표를 같은 기안서에 두 번 붙이지 못하게 한다(부분 유니크 — NULL 은 서로 충돌하지 않으므로 필요).
CREATE UNIQUE INDEX uk_adv_doc_sales    ON public.approval_document_vouchers (document_id, sales_id)    WHERE sales_id    IS NOT NULL;
CREATE UNIQUE INDEX uk_adv_doc_purchase ON public.approval_document_vouchers (document_id, purchase_id) WHERE purchase_id IS NOT NULL;
CREATE UNIQUE INDEX uk_adv_doc_expense  ON public.approval_document_vouchers (document_id, expense_id)  WHERE expense_id  IS NOT NULL;
