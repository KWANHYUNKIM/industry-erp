-- ApprovalFormType 은 분기 로직이 전혀 없다(코드 전역 검색으로 확인). 그래서 이 enum 은
-- 코드 마스터로 전환해도 안전한 유일한 케이스다. PartnerType·SettlementType 처럼
-- 로직을 지고 있는 enum 은 그대로 둔다.
--
-- form_type 컬럼은 이 마이그레이션에서 지우지 않는다. 백필 결과를 확인한 뒤 V14 에서 떨군다.

